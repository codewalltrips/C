#include<stdio.h>
 struct distance
 {
 	int distance1,distance2,sum;
 	
 }
 main()
 {
 	struct distance dis1,dis2,dis;
 	printf("Enter distance1 and distance2");
 	scanf("%d%d",&dis1.distance1,&dis2.distance2);
 	dis.sum=dis1.distance1+dis2.distance2;
 	printf("Display\n");
 	printf("\nSum=%d",dis.sum);
 }
